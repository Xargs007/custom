	c:RegisterEffect(e5)
	local e07=Effect.CreateEffect(c)
	e07:SetType(EFFECT_TYPE_SINGLE)
	e07:SetProperty(EFFECT_FLAG_SINGLE_RANGE)
	e07:SetRange(LOCATION_MZONE)
	e07:SetCode(EFFECT_ADD_ATTRIBUTE)
	e07:SetValue(0x10)
	c:RegisterEffect(e07)
	--spsummon count limit
	local e04=Effect.CreateEffect(c)
	e04:SetType(EFFECT_TYPE_FIELD)
	e04:SetCode(EFFECT_SPSUMMON_COUNT_LIMIT)
